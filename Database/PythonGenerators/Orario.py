from datetime import datetime, timedelta
import random


GIORNI = ["Lunedì", "Martedì", "Mercoledì", "Giovedì", "Venerdì", "Sabato"]

SLOT_ORARI = [
    ("09:00", "11:00"),
    ("11:00", "12:00"),
    ("13:00", "15:00"),
    ("15:00", "17:00"),
]

def parse(date):
    return datetime.strptime(date, "%Y-%m-%d")

def overlap(a_start, a_end, b_start, b_end):
    return a_start < b_end and b_start < a_end

orari_generati = []

for mod in moduli:
    codice_cor, cod_mat_anno, cod_modulo, d_inizio, d_fine, desc, tit = mod

    # risalgo alla materia tramite Materia_Anno
    codice_materia = next(
        (ma[1] for ma in materia_anno if ma[0] == cod_mat_anno),
        None
    )

    if codice_materia is None:
        print(f"⚠️ Cod_Mat_Anno {cod_mat_anno} non trovato, corso {codice_cor} saltato")
        continue

    # sede della materia
    sede = next(
        (m[2] for m in materie if m[0] == codice_materia),
        None
    )

    if sede is None:
        print(f"⚠️ Materia {codice_materia} non trovata, corso {codice_cor} saltato")
        continue

    # aule compatibili
    aule = [c for c in classi if c[0] == sede]

    if not aule:
        continue

    lezioni_sett = random.choice([2, 3])
    giorni_scelti = random.sample(GIORNI, lezioni_sett)

    for giorno in giorni_scelti:
        slot = random.choice(SLOT_ORARI)
        aula = random.choice(aule)

        start = datetime.strptime(slot[0], "%H:%M")
        end = datetime.strptime(slot[1], "%H:%M")

        ok = True
        for o in orari_generati:
            # stesso corso + stessa materia/anno
            if o["corso"] == codice_cor and o["Mat_anno"] == cod_mat_anno:
                if overlap(start, end, o["start"], o["end"]):
                    ok = False

            # stessa aula
            if o["uni"] == aula[0] and o["stanza"] == aula[1]:
                if overlap(start, end, o["start"], o["end"]):
                    ok = False

        if ok:
            orari_generati.append({
                "corso": codice_cor,
                "Mat_anno": cod_mat_anno,
                "Modulo": cod_modulo,
                "giorno": giorno,
                "start": start,
                "end": end,
                "uni": aula[0],
                "stanza": aula[1]
            })

codice_corrente = 0

